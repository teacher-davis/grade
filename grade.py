score = int(input("Enter Your Score to get Grade: "))

if score >= 80 and score <= 100:
    print("Grade A")

elif score >= 70 and score <= 80:
    print("Grade B")

elif score >= 60 | score <= 50:
    print("Grade C")

elif score >= 50 | score <= 60:
    print("Grade C")

elif score >= 40 | score <= 50:
    print("Grade D")

else:
    print("grade E")               